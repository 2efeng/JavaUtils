package com.hzf.utils.Comparator;

public class Bean {

    private int numInt;
    private String numStr;

    public Bean(int numInt, String numStr) {
        this.numInt = numInt;
        this.numStr = numStr;
    }

    public int getNumInt() {
        return numInt;
    }

    public void setNumInt(int numInt) {
        this.numInt = numInt;
    }

    public String getNumStr() {
        return numStr;
    }

    public void setNumStr(String numStr) {
        this.numStr = numStr;
    }
}
